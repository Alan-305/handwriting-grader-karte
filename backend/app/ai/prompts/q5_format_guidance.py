"""第5問生成で共通テスト型に寄らないための共有文言。"""

ANTI_KYOTSU_Q5_BLOCK = """
【重要 — 大学入学共通テスト第5問ではない】
- 作成対象は **志望校の二次入試英語・第5問** であり、大学入学共通テスト（共通テスト）の第5問そのものではない
- 共通テスト専用の定型を機械的に使わないこと:
  - 出来事4つの「正しい順に並べたものを選べ」一問目固定
  - Story Map（物語地図）表・空欄表の設問
  - 「物語のメッセージ／主人公の学び」を問5に固定
  - 指示文だけ「次の物語を読み、あとの問いに答えなさい。」で終わる共通テスト標準様式
- **参照過去問（同大学の第5問）** がある場合は、設問数・指示の言い回し・技能・選択肢形式をそこに合わせる（本文・題材は新規）
- 小問数は **6〜8個**。英文の内容に合わせて技能を組み合わせる
- **各小問が参照する本文箇所・下線・空所・ヒント語句は小問間で重複させない**（passageAnchor で明示）
""".strip()


def format_reference_block(*, university_name: str, reference_context: str) -> str:
    if not reference_context.strip():
        return ""
    uni = university_name.strip() or "志望校"
    return (
        f"\n\n【参照過去問 — {uni}・第5問（形式の手本。本文・題材・人名はコピー禁止）】\n"
        f"{reference_context.strip()}\n"
        "上記は**東大／当該大学の入試**の第5問である。共通テストの第5問の型に置き換えないこと。"
    )
